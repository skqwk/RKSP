package practice3.indicator;


public class TempAndSmoke {
    private final int temp;
    private final int smoke;

    public TempAndSmoke(int temp, int smoke) {
        this.temp = temp;
        this.smoke = smoke;
    }

    public int getTemp() {
        return temp;
    }

    public int getSmoke() {
        return smoke;
    }
}
